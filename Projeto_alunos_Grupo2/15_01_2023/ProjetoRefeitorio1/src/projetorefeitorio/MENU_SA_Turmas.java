 private void preencheTabela() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            Connection conn = LigaBD.ligacao();
            String sql = "SELECT * FROM turma";
            PreparedStatement ps = conn.prepareStatement(sql, ResultSet.TYPE_SCROLL_SENSITIVE,
                    ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = ps.executeQuery();
            rs.first();

            int rowCount = modelo.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
            do {
                modelo.addRow(new Object[]{rs.getInt("idTurma"), rs.getString("cod_curso"), rs.getDate("data_inicio"), rs.getDate("data_fim"), rs.getString("descricao")});
            } while (rs.next());

        } catch (Exception e) {
            Logger.getLogger(MENU_SA_Turmas.class.getName()).log(Level.SEVERE, null, e);
        }
    }
