    private void Btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_LoginActionPerformed
        String login = ctxLogin.getText();
        String pass = ctxPassword.getText();
        try {
            if (VerificaLogin(login, pass)) {
                verifica = VerificaAdmin(login);
                switch (verifica) {
                    case 1:
                        mensageminfo("Bem-vindo " + login + "!");
                        MENU_S_Admin sa = new MENU_S_Admin();
                        this.setVisible(false);
                        sa.setVisible(true);
                        break;
                    case 2: {
                        mensageminfo("Bem-vindo " + login + "!");
                        MENU_A_Relatorio mo = new MENU_A_Relatorio();
                        this.setVisible(false);
                        mo.setVisible(true);
                        break;
                    }
                    case 3: {
                        mensageminfo("Bem-vindo " + login + "!");
                        MENU_AG_Relatorio mo = new MENU_AG_Relatorio();
                        this.setVisible(false);
                        mo.setVisible(true);
                        break;
                    }
                    default:
                        break;

                }
            } else {
                mensagemErro("Dados Incorretos!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login1.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Btn_LoginActionPerformed

